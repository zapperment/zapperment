const {
  isControlled,
  isControlledByButton,
  isNumber,
  getRandomBool,
  getRandomEntry,
  getRandomInt,
  interpolateMidiValue
} = require("./utils");

const controllers = {
  rotary1: 71,
  rotary2: 72,
  rotary3: 73,
  rotary4: 74,
  button1: 75,
  button2: 76,
  button3: 77,
  button4: 78
};

module.exports = convertedTrackWithDefaults => {
  const midiData = [];
  const scene = { channels: [] };
  for (const {
    midi: { bus, channel },
    playing,
    meta: { name }
  } of convertedTrackWithDefaults.channels) {
    let sceneValue = playing;
    let isPlaying = playing;
    if (isControlled(playing)) {
      for (const [control, options] of Object.entries(playing)) {
        let controllerValue;
        if (isControlledByButton(playing)) {
          const isOn = getRandomBool();
          if (isOn) {
            sceneValue = options.on;
            controllerValue = 127;
          } else {
            sceneValue = options.off;
            controllerValue = 0;
          }
        } else {
          const { min, max } = options;
          if (min && max) {
            if (!isNumber(min) || !isNumber(max)) {
              throw new Error(
                `Expected numerical values for min and max, received min=${typeof min}, max=${typeof max}`
              );
            }
            if (min > max) {
              throw new Error(
                `Expected min to be less than or equal to max, received min=${min}, max=${max}`
              );
            }
            controllerValue = getRandomInt(0, 127);
            sceneValue = interpolateMidiValue(controllerValue, min, max);
          } else {
            let key;
            [key, sceneValue] = getRandomEntry(options);
            controllerValue = parseInt(key, 10);
          }
        }
        midiData.push({
          bus,
          channel,
          controller: controllers[control],
          value: controllerValue
        });
        isPlaying = controllerValue === 127;
      }
      if (isPlaying) {
        scene.channels.push(name);
      }
    }
  }
  return { scene, midiData };
};

